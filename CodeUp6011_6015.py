# 6011
# 변수에 실수값을 저장한 후
# 변수에 저장되어 있는 값을 그대로 출력해보자.

# 예시
# f = input()
# f = float(f)
# print(f)
# 와 같은 형태로 가능하다.

f = input()
f = float(f)
print(f)





# 6012
# 줄을 바꿔 정수(integer) 2개를 입력받아 줄을 바꿔 출력해보자.

# 예시
# a = input() 
# b = input()
# a=int(a)
# b=int(b)
# print(a)
# print(b)
# 과 같은 방법으로 가능하다.

a = int(input())
b = int(input())
print(a)
print(b)




